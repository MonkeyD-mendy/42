int		ft_str_is_alpha(char *str)
{
	int	a;

	a = 0;
	while (str[a])
	{
		if (!(str[a] >= 97 && str[a] <= 122) && !(str[a] >= 65 && str[a] <= 90))
			return (0);
		a++;
	}
	return (1);
}
